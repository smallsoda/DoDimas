/* USER CODE BEGIN 4 */
uint8_t *frame (uint8_t *pdata, uint8_t *size) {

		uint8_t buff[256];
		
		buff[0]=0x00;									/*Preamble*/
		buff[1]=0x00;	
		buff[2]=0xFF;									/*Start code*/
		buff[3]=*size+1;	 							/*Lenght with TFI (D4)*/
		buff[4]= ~buff[3] + 1 ;				/*Checksum Lenght*/
		buff[5]=0xD4;	 								/*TFI from host to pn532*/
	
		uint8_t j=6;
	
	for (uint8_t i=0; i<*size; i++) {
		
			buff[j]=*pdata;
			pdata++;
			j++;
	}
	
		uint8_t sum=0;
	
	for (uint8_t i=5; i<j; i++) {
	
		sum += buff[i];
	}
	
		buff[j]= ~sum+1; 							/*Data Checksum (TFI+PD0+..+PDn)*/
		buff[++j]=0x00;								/*Postamble*/
	
		uint8_t *result;
		result = &buff[0];
	
		*size= ++j;
	
		return result;
}

